sites_df = (
    spark.read
        .option("header", "true")
        .option("inferSchema", "true")
        .csv(f"{VOLUME_PATH}/site_lat_long.csv")
)

(sites_df.write
    .mode("overwrite")
    .option("overwriteSchema", "true")
    .saveAsTable("sites_bronze"))

print(f"sites_bronze: {spark.table('sites_bronze').count():,} rows")
display(spark.table("sites_bronze").limit(5))

