# MAGIC %md
# MAGIC ## MapInfo TAB polygons → `county_markets_bronze`
# MAGIC
# MAGIC `geopandas.read_file` opens the `.TAB` (and the DAT/MAP/ID/IND sidecars) transparently.
# MAGIC Polygons are reprojected to WGS84 and simplified to ~100 m tolerance — fine for
# MAGIC "is this site inside this market" determination and keeps each row under Spark's
# MAGIC per-row buffer limit.

# COMMAND ----------

import geopandas as gpd
from pyspark.sql.types import StructType, StructField, StringType, IntegerType

gdf = gpd.read_file(f"{VOLUME_PATH}/Top100_SmallRural_County_Markets_2021.TAB")
print(f"Polygons read: {len(gdf):,}  |  source CRS: {gdf.crs}")

gdf_wgs84 = gdf.to_crs(epsg=4326)
gdf_wgs84["geometry"] = gdf_wgs84["geometry"].simplify(tolerance=0.001, preserve_topology=True)
gdf_wgs84["geometry_wkt"] = gdf_wgs84["geometry"].apply(lambda g: g.wkt if g is not None else None)
print(f"Max WKT length after simplify: {gdf_wgs84['geometry_wkt'].str.len().max():,} chars")

schema = StructType([
    StructField("ID", StringType(), True),
    StructField("Name", StringType(), True),
    StructField("State", StringType(), True),
    StructField("MarketType", StringType(), True),
    StructField("MarketTypeDetailed", StringType(), True),
    StructField("Population", IntegerType(), True),
    StructField("geometry_wkt", StringType(), True),
])

pdf = gdf_wgs84.drop(columns=["geometry"])
markets_df = spark.createDataFrame(pdf[[f.name for f in schema.fields]], schema=schema)

(markets_df.repartition(16).write
    .mode("overwrite")
    .option("overwriteSchema", "true")
    .saveAsTable("county_markets_bronze"))

print(f"county_markets_bronze: {spark.table('county_markets_bronze').count():,} rows")
display(spark.table("county_markets_bronze").limit(5))

